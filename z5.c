#include <stdio.h>

int main() {
    int n, count = 0;

    // Ввод числа n
    printf("Введите число n: ");
    scanf("%d", &n);

    // Проверка всех чисел от 1 до n
    for (int i = 1; i <= n; i++) {
        // Если число не делится на 5 или на 11, увеличиваем счетчик
        if (i % 5 != 0 && i % 11 != 0) {
            count++;
        }
    }

    // Вывод результата
    printf("Количество чисел между 1 и %d, которые не делятся на 5 или 11: %d\n", n, count);

    return 0;
}
